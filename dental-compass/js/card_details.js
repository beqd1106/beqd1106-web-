// ─── HTML レンダラー ───────────────────────────────
function buildDetailHtml(data) {
  let html = '<div class="card-detail-inner">';

  if (data.lead) {
    html += `<p class="detail-lead">${data.lead}</p>`;
  }

  if (data.sections) {
    data.sections.forEach(sec => {
      html += `<div class="detail-section">`;
      if (sec.heading) html += `<h5 class="detail-heading">${sec.heading}</h5>`;

      if (sec.type === 'table') {
        html += `<div class="detail-table-wrap"><table class="detail-table">`;
        if (sec.headers) {
          html += `<thead><tr>${sec.headers.map(h => `<th>${h}</th>`).join('')}</tr></thead>`;
        }
        html += `<tbody>${sec.rows.map(r => `<tr>${r.map((c, i) => {
          const cls = sec.highlight && sec.highlight.includes(i) ? ' class="highlight"' : '';
          return `<td${cls}>${c}</td>`;
        }).join('')}</tr>`).join('')}</tbody></table></div>`;
      }

      if (sec.type === 'list') {
        html += `<ul class="detail-list">${sec.items.map(item =>
          typeof item === 'string'
            ? `<li>${item}</li>`
            : `<li><strong>${item.label}：</strong>${item.text}</li>`
        ).join('')}</ul>`;
      }

      if (sec.type === 'steps') {
        html += `<ol class="detail-steps">${sec.items.map(item =>
          `<li><span class="step-label">${item.step}</span><div><strong>${item.title}</strong><p>${item.desc}</p></div></li>`
        ).join('')}</ol>`;
      }

      if (sec.type === 'alert') {
        const icons = { info:'ℹ️', warn:'⚠️', danger:'🚨', success:'✅' };
        html += `<div class="detail-alert detail-alert-${sec.level}"><span>${icons[sec.level]||'ℹ️'}</span><span>${sec.text}</span></div>`;
      }

      if (sec.type === 'links') {
        html += `<div class="detail-links">`;
        sec.items.forEach(lnk => {
          html += `<a href="${lnk.url}" target="_blank" rel="noopener" class="detail-link-btn">
            <span class="dl-icon">${lnk.icon || '🔗'}</span>
            <span class="dl-text"><strong>${lnk.label}</strong><small>${lnk.desc || ''}</small></span>
            <span class="dl-arrow">↗</span>
          </a>`;
        });
        html += `</div>`;
      }

      html += `</div>`;
    });
  }

  html += '</div>';
  return html;
}

