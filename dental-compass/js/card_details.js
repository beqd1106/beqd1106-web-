// ─── HTML レンダラー ───────────────────────────────
// cardKey を第2引数で受け取り CARD_META から情報品質バッジを表示する
function buildDetailHtml(data, cardKey) {
  let html = '<div class="card-detail-inner">';

  // ── 情報品質ストリップ ──────────────────────────
  const meta = (typeof CARD_META !== 'undefined' && cardKey && CARD_META[cardKey])
    ? CARD_META[cardKey] : (data._meta || null);
  if (meta) {
    const TYPE_CFG = {
      legal:    '法令根拠',
      official: '公的機関データ',
      estimate: '市場目安・参考値',
      guidance: '実務ガイダンス',
    };
    const typeLabel = TYPE_CFG[meta.type] || '一般情報';
    const v = meta.verified;
    // 18ヶ月を超えたら「要再確認」
    const isStale = v ? (Date.now() - new Date(v).getTime()) > 548 * 864e5 : false;

    html += `<div class="detail-meta-strip">
      <span class="dmeta-badge dmeta-${meta.type || 'guidance'}">${typeLabel}</span>`;

    if (v) {
      html += `<span class="dmeta-date${isStale ? ' dmeta-stale' : ''}">最終確認: ${v}${isStale ? ' ⚠ 要再確認' : ''}</span>`;
    } else {
      html += `<span class="dmeta-date dmeta-unverified">出典要確認</span>`;
    }

    if (meta.volatility === 'high') {
      html += `<span class="dmeta-volatile">要定期更新</span>`;
    }

    if (meta.note) {
      html += `<span class="dmeta-note">${meta.note}</span>`;
    }

    html += `</div>`;
  }

  if (data.lead) {
    html += `<p class="detail-lead">${data.lead}</p>`;
  }

  if (data.sections) {
    data.sections.forEach(sec => {
      html += `<div class="detail-section">`;
      if (sec.heading) html += `<h5 class="detail-heading">${sec.heading}</h5>`;

      if (sec.type === 'table') {
        html += `<div class="detail-table-wrap"><table class="detail-table">`;
        if (sec.headers) {
          html += `<thead><tr>${sec.headers.map(h => `<th>${h}</th>`).join('')}</tr></thead>`;
        }
        html += `<tbody>${sec.rows.map(r => `<tr>${r.map((c, i) => {
          const cls = sec.highlight && sec.highlight.includes(i) ? ' class="highlight"' : '';
          return `<td${cls}>${c}</td>`;
        }).join('')}</tr>`).join('')}</tbody></table></div>`;
      }

      if (sec.type === 'list') {
        html += `<ul class="detail-list">${sec.items.map(item =>
          typeof item === 'string'
            ? `<li>${item}</li>`
            : `<li><strong>${item.label}：</strong>${item.text}</li>`
        ).join('')}</ul>`;
      }

      if (sec.type === 'steps') {
        html += `<ol class="detail-steps">${sec.items.map(item =>
          `<li><span class="step-label">${item.step}</span><div><strong>${item.title}</strong><p>${item.desc}</p></div></li>`
        ).join('')}</ol>`;
      }

      if (sec.type === 'alert') {
        const icons = { info:'ℹ️', warn:'⚠️', danger:'🚨', success:'✅' };
        const alerts = sec.items ? sec.items : [{ level: sec.level || 'info', text: sec.text }];
        alerts.forEach(a => {
          html += `<div class="detail-alert detail-alert-${a.level}"><span>${icons[a.level]||'ℹ️'}</span><span>${a.text}</span></div>`;
        });
      }

      if (sec.type === 'links') {
        html += `<div class="detail-links">`;
        sec.items.forEach(lnk => {
          html += `<a href="${lnk.url}" target="_blank" rel="noopener" class="detail-link-btn">
            <span class="dl-icon">${lnk.icon || '🔗'}</span>
            <span class="dl-text"><strong>${lnk.label}</strong><small>${lnk.desc || ''}</small></span>
            <span class="dl-arrow">↗</span>
          </a>`;
        });
        html += `</div>`;
      }

      // ── セクション別出典表示 ─────────────────────
      if (sec._source) {
        html += `<div class="detail-source-cite">
          <span class="source-tag">出典</span>
          <span class="source-body">${
            sec._source.url
              ? `<a href="${sec._source.url}" target="_blank" rel="noopener">${sec._source.label}</a>`
              : sec._source.label
          }${sec._source.accessed
            ? `<span class="source-date">（${sec._source.accessed}確認）</span>` : ''
          }</span>${
            sec._source.note
              ? `<span class="source-note">${sec._source.note}</span>` : ''
          }
        </div>`;
      }

      html += `</div>`;
    });
  }

  html += '</div>';
  return html;
}

