// カテゴリセクションのレンダリング
// =====================================================
function renderCategories() {
  const container = document.getElementById('cat-sections-container');
  if (!container) return;

  let html = '';
  CATEGORIES.forEach((cat, idx) => {
    const isEven = idx % 2 === 0;
    const alertHtml = cat.alert ? `
      <div class="alert alert-${cat.alert.type}" style="margin-top:1.5rem;">
        <span class="alert-icon">${cat.alert.type === 'info' ? 'ℹ' : cat.alert.type === 'warn' ? '<i class="ic ic-alert"></i>' : cat.alert.type === 'danger' ? '<i class="ic ic-alert"></i>' : '<i class="ic ic-check"></i>'}</span>
        <span>${cat.alert.text}</span>
      </div>` : '';

    const TOOL_MAP = {
      'cat-01': { id: 'tool1', label: '<i class="ic ic-tool"></i> 開業資金シミュレーターを試す' },
      'cat-02': { id: 'tool2', label: '<i class="ic ic-tool"></i> 月商・利益シミュレーターを試す' },
      'cat-03': { id: 'tool3', label: '<i class="ic ic-tool"></i> 開業スケジュール自動生成を試す' },
      'cat-04': { id: 'tool4', label: '<i class="ic ic-tool"></i> 行政届出チェックリストを試す' },
      'cat-05': { id: 'tool5', label: '<i class="ic ic-tool"></i> 施設基準・加算チェックツールを試す' },
      'cat-07': { id: 'tool10', label: '<i class="ic ic-tool"></i> 収益改善セルフチェックを試す' },
      'cat-08': { id: 'tool7', label: '<i class="ic ic-tool"></i> 医療広告ガイドラインチェッカーを試す' },
      'cat-10': { id: 'tool9', label: '<i class="ic ic-tool"></i> 求人票ジェネレーターを試す' },
      'cat-15': { id: 'tool6', label: '<i class="ic ic-tool"></i> KPIダッシュボードを試す' },
    };
    const toolEntry = TOOL_MAP[cat.id];
    const toolBadge = toolEntry
      ? `<button class="tool-link-badge" onclick="event.stopPropagation();openModal('${toolEntry.id}')">${toolEntry.label} →</button>`
      : cat.id === 'cat-06' ? '<span class="tag gold">令和8年度改定対応</span>'
      : cat.id === 'cat-14' ? '<span class="tag navy">セキュリティ対応</span>' : '';

    const cardsHtml = cat.cards.map((card, cardIdx) => {
      const key = `${cat.id}-${cardIdx}`;
      const hasDetail = typeof CARD_DETAILS !== 'undefined' && CARD_DETAILS[key];
      const hintHtml = hasDetail
        ? `<span class="card-expand-hint">詳しく見る <svg class="expand-chevron" width="13" height="13" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5"><polyline points="9 18 15 12 9 6"/></svg></span>`
        : '';
      return `
      <div class="info-card fade-up${hasDetail ? ' card-clickable' : ''}"
           ${hasDetail ? `onclick="openCardDrawer(this)"` : ''}
           data-card-key="${key}"
           data-card-title="${card.title}"
           data-cat-title="${cat.title}"
           data-cat-color="${cat.color}">
        <h4>${card.icon} ${card.title}</h4>
        <ul>${card.items.map(item => `<li>${item}</li>`).join('')}</ul>
        ${hintHtml}
      </div>`;
    }).join('');

    const revision2026Container = cat.id === 'cat-06' ? '<div id="revision2026-container" style="margin-top:2rem;"></div>' : '';

    html += `
      <section id="${cat.id}" class="cat-section section" style="${isEven ? '' : 'background:var(--gray-50);'}">
        <div class="container">
          <div class="cat-header fade-up">
            <div class="cat-icon-wrap" style="background:${cat.bg};color:${cat.color};">
              ${cat.icon}
            </div>
            <div>
              <span class="section-label">Category ${String(idx + 1).padStart(2, '0')}</span>
              <h2 class="cat-title">${cat.title}</h2>
              <p class="cat-overview">${cat.overview}</p>
              <div style="margin-top:0.75rem;">${toolBadge}</div>
            </div>
          </div>
          <div class="content-grid">${cardsHtml}</div>
          ${revision2026Container}
          ${alertHtml}
        </div>
      </section>`;
  });

  container.innerHTML = html;
  if (typeof initFadeObserver === 'function') initFadeObserver();
  if (typeof renderRevision2026Section === 'function') renderRevision2026Section();
}

